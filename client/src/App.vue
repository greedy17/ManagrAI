  methods: {
    showHelp() {
      this.showingHelp = true
    },
    hideHelp() {
      this.showingHelp = false
    },
    ...mapActions(['refreshCurrentUser']),
    openModal() {
      this.modalOpen = true
    },
    closeModal() {
      this.modalOpen = false
    },
    toggleMenu() {
      this.menuOpen = !this.menuOpen
    },
    closeMenu() {
      this.menuOpen = false
    },
    checkEnvironment() {
      const host = window.location.host
      let key
      if (host.includes('localhost') || host.includes('127.0.0.1')) {
        key =
          'pk_test_51OKmfeK7HCniMBUgPlsZADW5oDgfeqmrfxGPyMi8PMST4Vwtpx9UBArus6sL4wHXAYz2nohTtz1xg0LfOsRLDjZT00TyZn8QQe'
      } else if (host.includes('staging')) {
        key =
          'pk_test_51OKmcnJHm94PSCW8ruuXHTjqGMAOWitNe2IbTaddNXnrutuc8qtszxyuTjcaDNw2niLCDo8AiU1ErgzEKAlOd0ep00X9rd6FNW'
      } else {
        key =
          'pk_live_51OF1jYHvb6ZAgKwka9s7I7e3dBWwE6ettoQvdUsqLOyFKCeJARsy9J06bLSWfOkEikTihykhUNNTJ4wvGGUetH4z00TlvK5vKy'
      }
      this.$store.dispatch('updateStripeKey', key)
    },
    checkTokenExpiry() {
      if (localStorage.getItem('tokenReceivedAt')) {
        const tokenReceivedAt = new Date(parseInt(localStorage.getItem('tokenReceivedAt')))
        const expiresAt = new Date(tokenReceivedAt.getTime() + 14400000)
        const currentTime = new Date()

        if (currentTime >= new Date(expiresAt.getTime() - 300000)) {
          this.showExpiryWarning()
        }

        if (currentTime >= expiresAt) {
          clearInterval(this.checkInterval)
          this.logOut()
        }
      } else {
        return
      }
    },
    showExpiryWarning() {
      this.showExpireModal = true
    },
    logOut() {
      this.showExpireModal = false
      localStorage.removeItem('token')
      localStorage.removeItem('tokenReceivedAt')
      this.$store.dispatch('logoutUser')
      this.$router.push({ name: 'Login' })
    },
    async refreshToken() {
      try {
        await User.api
          .refreshToken(localStorage.getItem('token'), this.user.id)
          .then((response) => {
            console.log('TOKEN RESPONSE', response)
            clearInterval(this.checkInterval)
            let token = response.data.token
            localStorage.setItem('token', token)
            this.$store.dispatch('updateUserToken', token)
            localStorage.setItem('tokenReceivedAt', Date.now().toString())
            this.checkTokenExpiry()
            this.checkInterval = setInterval(this.checkTokenExpiry, 60 * 1000)
          })
      } catch (e) {
        console.log(e)
      } finally {
        this.showExpireModal = false
      }
    },
  },
  computed: {
    ...mapGetters(['userIsLoggedIn']),
    hideNavBar() {
      return routesWithoutNavBar.includes(this.$route.name)
    },
    userIsLoggedIn() {
      return this.$store.getters.userIsLoggedIn
    },
    isOnboarding() {
